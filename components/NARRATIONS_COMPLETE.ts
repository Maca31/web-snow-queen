// CONFIGURACIÓN DE VOZ — Latinoamericana suave
// ─────────────────────────────────────────────
export function getLatinoVoice(): SpeechSynthesisVoice | null {
  const voices = window.speechSynthesis.getVoices();

  return (
    // 1. Google español de EEUU (suena muy natural y suave)
    voices.find(v => v.name.includes('Google') && v.lang === 'es-US') ||
    // 2. Dalia — voz femenina de Edge Online latinoamericana
    voices.find(v => v.name.toLowerCase().includes('dalia')) ||
    // 3. Paulina — voz femenina de macOS latinoamericana
    voices.find(v => v.name.toLowerCase().includes('paulina')) ||
    // 4. Sabina — voz femenina es-MX de Windows
    voices.find(v => v.name.toLowerCase().includes('sabina')) ||
    // 5. Monica — voz femenina suave
    voices.find(v => v.name.toLowerCase().includes('monica')) ||
    // 6. Cualquier voz es-MX
    voices.find(v => v.lang === 'es-MX') ||
    // 7. Cualquier voz es-US
    voices.find(v => v.lang === 'es-US') ||
    // 8. Cualquier español latinoamericano
    voices.find(v => ['es-AR', 'es-CO', 'es-CL', 'es-PE'].includes(v.lang)) ||
    // 9. Helena u otra femenina española como fallback
    voices.find(v => v.lang.startsWith('es') && ['helena', 'laura', 'elvira'].some(n => v.name.toLowerCase().includes(n))) ||
    // 10. Cualquier español como fallback
    voices.find(v => v.lang.startsWith('es')) ||
    null
  );
}

